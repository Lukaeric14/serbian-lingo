// Fisher-Yates shuffle — used to randomize word-bank/tile order so it doesn't
// leak the correct answer order (content data stores tiles in answer order).
export function shuffle<T>(items: T[]): T[] {
  const result = [...items];
  for (let i = result.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [result[i], result[j]] = [result[j], result[i]];
  }
  return result;
}
